    private void register_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_register_btnActionPerformed
        // TODO add your handling code here:
    String custID = cust_id_tf.getText().trim();
    String custName = cust_name_tf.getText().trim();
    String phone = cust_phone_tf.getText().trim();
    String address = cust_add_tf.getText().trim();
    String email = cust_email_tf.getText().trim(); // Assuming email is autofilled
    String agencyID = (String) agency_id_drop.getSelectedItem();

    // Validate input data
    if (custID.isEmpty() || custName.isEmpty() || phone.isEmpty() || address.isEmpty() || email.isEmpty() || agencyID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Additional validation logic can be added here (e.g., email format, phone number format)

    try {
        // Prepare SQL statement for insertion
        String insertCustomerSQL = "INSERT INTO Customer(cust_ID, cust_name, phone, address, email, Agency_ID) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement psCustomer = customer.prepareStatement(insertCustomerSQL);
        psCustomer.setString(1, custID);
        psCustomer.setString(2, custName);
        psCustomer.setString(3, phone);
        psCustomer.setString(4, address);
        psCustomer.setString(5, email);
        psCustomer.setString(6, agencyID);

        // Execute the SQL statement
        int rowsAffected = psCustomer.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Customer Record Saved Successfully");
            
            // Optionally, clear input fields after successful insertion
            cust_id_tf.setText("");
            cust_name_tf.setText("");
            cust_phone_tf.setText("");
            cust_add_tf.setText("");
            // Do not clear email field since it's autofilled
        } else {
            JOptionPane.showMessageDialog(this, "Failed to save customer record", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "SQL Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace(); // Print stack trace for debugging
    }
    }//GEN-LAST:event_register_btnActionPerformed

