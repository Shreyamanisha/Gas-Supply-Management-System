    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed

    String custID = cust_id_tf.getText().trim();
    String custName = cust_name_tf.getText().trim();
    String phone = cust_phone_tf.getText().trim();
    String address = cust_add_tf.getText().trim();
    String email = cust_email_tf.getText().trim(); // Assuming email is autofilled
    String agencyID = (String) agency_id_drop.getSelectedItem();

    if (custID.isEmpty() || custName.isEmpty() || phone.isEmpty() || address.isEmpty() || email.isEmpty() || agencyID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        String updateSQL = "UPDATE Customer SET cust_name = ?, phone = ?, address = ?, email = ?, Agency_ID = ? WHERE cust_ID = ?";
        PreparedStatement ps = customer.prepareStatement(updateSQL);
        ps.setString(1, custName);
        ps.setString(2, phone);
        ps.setString(3, address);
        ps.setString(4, email);
        ps.setString(5, agencyID);
        ps.setString(6, custID);
        
        int rowsUpdated = ps.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Customer updated successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Customer not found");
        }
    } catch (SQLException ex) {
        Logger.getLogger(HomePage.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "SQL Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_update_btnActionPerformed

